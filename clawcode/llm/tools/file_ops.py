class ViewTool(BaseTool):
    """Tool for viewing file contents."""

    def __init__(self, permissions: Any = None) -> None:
        """Initialize the view tool.

        Args:
            permissions: Permission service for file access
        """
        self._permissions = permissions

    def info(self) -> ToolInfo:
        """Get tool information.

        Returns:
            ToolInfo describing this tool
        """
        return ToolInfo(
            name="view",
            description="View the contents of a file. "
            "Use this to read file contents with optional offset and limit for pagination.",
            parameters={
                "type": "object",
                "properties": {
                    "file_path": {
                        "type": "string",
                        "description": "Path to the file to view.",
                    },
                    "offset": {
                        "type": "integer",
                        "description": "Line number to start from (0-indexed, default: 0).",
                    },
                    "limit": {
                        "type": "integer",
                        "description": "Maximum number of lines to read (default: 0 = all).",
                    },
                },
                "required": ["file_path"],
            },
            required=["file_path"],
        )

    async def run(
        self,
        call: ToolCall,
        context: ToolContext,
    ) -> ToolResponse:
        """View file contents.

        Args:
            call: Tool call with file path
            context: Tool execution context

        Returns:
            Tool response with file contents
        """
        # Reuse advanced coercion (lazy import avoids cycle at module load).
        from .advanced import _coerce_tool_params, _get_param

        params = _coerce_tool_params(call)
        file_path = _get_param(params, "file_path", "filePath", "path", "filename")
        offset = params.get("offset", 0)
        limit = params.get("limit", 0)

        if not file_path:
            return ToolResponse(
                content="Error: No file path provided",
                is_error=True,
            )

        path = resolve_tool_path(file_path, context.working_directory)

        # Check if file exists
        if not path.exists():
            return ToolResponse(
                content=f"Error: File not found: {file_path}",
                is_error=True,
            )

        if not path.is_file():
            return ToolResponse(
                content=f"Error: Not a file: {file_path}",
                is_error=True,
            )

        try:
            lines = await asyncio.to_thread(_read_lines_sync, path, offset, limit)
            # Register the read so this session may modify the file.
            from .file_guard import record_read

            record_read(context.session_id, path)
            return ToolResponse(
                content=sanitize_text("\n".join(lines)),
                metadata=f"Read {len(lines)} lines from {file_path}",
            )
        except Exception as e:
            return ToolResponse(
                content=f"Error reading file: {e}",
                is_error=True,
            )

    # Path resolution delegated to module-level resolve_tool_path()


