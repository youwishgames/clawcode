def _read_lines_sync(
    path: Path, offset: int, limit: int
) -> list[str]:
    """Read lines from *path* with offset/limit, using binary seek for large offsets."""
    with open(path, "rb") as fb:
        if offset > 0:
            if offset > 500:
                pos = 0
                data = fb.read()
                for _ in range(offset):
                    nl = data.find(b"\n", pos)
                    if nl == -1:
                        return []
                    pos = nl + 1
                remainder = data[pos:]
            else:
                lines_skipped = 0
                while lines_skipped < offset:
                    if fb.readline() == b"":
                        return []
                    lines_skipped += 1
                remainder = fb.read()
        else:
            remainder = fb.read()
    text = remainder.decode("utf-8", errors="replace")
    all_lines = text.splitlines()
    if limit > 0:
        all_lines = all_lines[:limit]
    return [ln.rstrip("\r") for ln in all_lines]


def _workspace_root_guess() -> Path | None:
    """Best-effort guess of the repo/workspace root.

    Used to recover from stale absolute paths that point to a different clone,
    e.g. ``C:\\Codes\\clawcode\\...`` while the current workspace lives elsewhere.
    """
    try:
        outer_clawcode_dir = Path(__file__).resolve().parents[3]
        return outer_clawcode_dir.parent
    except Exception:
        return None


def resolve_tool_path(file_path: str, working_directory: str) -> Path:
    """Resolve a possibly-stale file path to an existing location.

    Shared by all file-touching tools (view, write, edit, patch, etc.) so
    that paths generated by the LLM during earlier sessions (e.g.
    ``C:\\Codes\\clawcode\\...``) are transparently mapped to the current
    workspace.
    """
    raw = (file_path or "").strip().strip("\"'")
    if not raw:
        return Path(file_path)

    # Recover workspace-relative pseudo-paths occasionally produced by the LLM,
    # e.g. "-p/clawcode/..." or "/p/docs/...".
    ws_root = _workspace_root_guess()
    for prefix in ("-p/", "/p/"):
        if raw.startswith(prefix) and ws_root is not None:
            return (ws_root / Path(raw[len(prefix):])).resolve()

    raw = os.path.expandvars(os.path.expanduser(raw))
    p = Path(raw)

    # 1) Relative path: resolve against working_directory.
    if not p.is_absolute():
        try:
            wd = Path(working_directory) if working_directory else Path.cwd()
            cand = (wd / p).resolve()
            if cand.exists():
                return cand
            # Even if it doesn't exist yet (write tool), return resolved form.
            return cand
        except Exception:
            pass

    # 2) Absolute but missing: try to map to this workspace clone.
    if p.is_absolute() and not p.exists():
        ws_root = _workspace_root_guess()
        if ws_root is not None:
            parts = list(p.parts)
            lowered = [str(x).lower() for x in parts]
            if "clawcode" in lowered:
                idx = lowered.index("clawcode")
                tail = Path(*parts[idx:])
                cand = (ws_root / tail)
                if cand.exists():
                    return cand
                # For write: return mapped path even if it doesn't exist yet.
                return cand

            for needle in ("clawcode\\clawcode", "clawcode/clawcode"):
                s = str(p)
                pos = s.lower().find(needle)
                if pos >= 0:
                    tail_str = s[pos:].replace("\\", "/")
                    cand2 = ws_root / Path(tail_str)
                    if cand2.exists():
                        return cand2
                    return cand2

    return p


def assert_resolved_path_in_workspace(
    resolved: Path,
    working_directory: str,
) -> str | None:
    """Return an error message if *resolved* is outside *working_directory*, else None.

    Used by write/edit/patch to prevent the model from targeting arbitrary absolute
    paths (e.g. another clone) when the session cwd is ``-c`` / project root.
    """
    wd_raw = (working_directory or "").strip()
    if not wd_raw:
        return None
    try:
        root = Path(wd_raw).expanduser().resolve()
        cand = resolved.resolve()
    except (OSError, RuntimeError):
        return None
    try:
        if cand == root:
            return None
        cand.relative_to(root)
        return None
    except ValueError:
        return (
            f"Path is outside the session project root ({root}). "
            f"Refusing to modify: {cand}. "
            "Use a path under the project root or a relative path."
        )


