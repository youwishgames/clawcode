class BatchViewTool(BaseTool):
    """Read multiple files (or file sections) in a single tool call."""

    def __init__(self, permissions: Any = None) -> None:
        self._permissions = permissions

    def info(self) -> ToolInfo:
        return ToolInfo(
            name="batch_view",
            description=(
                "Read multiple files in one call. Each entry can specify "
                "offset (0-indexed line) and limit. More efficient than "
                "multiple sequential view calls."
            ),
            parameters={
                "type": "object",
                "properties": {
                    "files": {
                        "type": "array",
                        "items": {
                            "type": "object",
                            "properties": {
                                "file_path": {"type": "string"},
                                "offset": {"type": "integer", "default": 0},
                                "limit": {"type": "integer", "default": 0},
                            },
                            "required": ["file_path"],
                        },
                        "description": "List of files to read with optional offset/limit.",
                    },
                },
                "required": ["files"],
            },
            required=["files"],
        )

    async def run(self, call: ToolCall, context: ToolContext) -> ToolResponse:
        from .advanced import _coerce_tool_params

        params = _coerce_tool_params(call)
        files_spec = params.get("files")
        if not isinstance(files_spec, list) or not files_spec:
            return ToolResponse(content="Error: 'files' must be a non-empty array.", is_error=True)

        async def _read_one(spec: dict) -> str:
            fp = str(spec.get("file_path") or "")
            off = int(spec.get("offset", 0) or 0)
            lim = int(spec.get("limit", 0) or 0)
            if not fp:
                return f"--- (no file_path) ---\nError: missing file_path"
            path = resolve_tool_path(fp, context.working_directory)
            if not path.exists():
                return f"--- {fp} ---\nError: File not found"
            if not path.is_file():
                return f"--- {fp} ---\nError: Not a file"
            lines = await asyncio.to_thread(_read_lines_sync, path, off, lim)
            # Register the read so this session may modify the file.
            from .file_guard import record_read

            record_read(context.session_id, path)
            header = f"--- {fp} L{off + 1}-{off + len(lines)} ({len(lines)} lines) ---"
            return header + "\n" + "\n".join(lines)

        parts = await asyncio.gather(*[_read_one(s) for s in files_spec])
        return ToolResponse(
            content=sanitize_text("\n\n".join(parts)),
            metadata=f"Read {len(files_spec)} files",
        )


class LsTool(BaseTool):
    """Tool for listing directory contents."""

    IGNORE_PATTERNS = {
        "__pycache__",
        ".git",
        ".svn",
        "node_modules",
        ".venv",
        "venv",
        ".env",
        "*.pyc",
        ".DS_Store",
    }

    def __init__(self, permissions: Any = None) -> None:
        """Initialize the ls tool.

        Args:
            permissions: Permission service for directory access
        """
        self._permissions = permissions

    def info(self) -> ToolInfo:
        """Get tool information.

        Returns:
            ToolInfo describing this tool
        """
        return ToolInfo(
            name="ls",
            description="List files and directories in a path. "
            "Use this to see what files are available in a directory.",
            parameters={
                "type": "object",
                "properties": {
                    "path": {
                        "type": "string",
                        "description": "Path to list (default: current directory).",
                    },
                    "recursive": {
                        "type": "boolean",
                        "description": "Recursively list files (default: false).",
                    },
                },
                "required": [],
            },
        )

    async def run(
        self,
        call: ToolCall,
        context: ToolContext,
    ) -> ToolResponse:
        """List directory contents.

        Args:
            call: Tool call with path parameters
            context: Tool execution context

        Returns:
            Tool response with directory listing
        """
        params = call.input if isinstance(call.input, dict) else {}
        path_str = params.get("path", ".")
        recursive = params.get("recursive", False)

        path = resolve_tool_path(path_str, context.working_directory)

        # Check if path exists
        if not path.exists():
            return ToolResponse(
                content=f"Error: Path not found: {path_str}",
                is_error=True,
            )

        try:
            if path.is_file():
                # Return just the file info
                size = path.stat().st_size
                return ToolResponse(
                    content=f"- {path.name} ({size} bytes)",
                    metadata=f"1 file in {path_str}",
                )

            # List directory
            if recursive:
                items = self._list_recursive(path)
            else:
                items = self._list_directory(path)

            # Format output
            lines = []
            for item_type, item_path, size in items:
                if item_type == "DIR":
                    lines.append(f"📁 {item_path}/")
                else:
                    size_str = f" ({size} bytes)" if size else ""
                    lines.append(f"📄 {item_path}{size_str}")

            return ToolResponse(
                content="\n".join(lines),
                metadata=f"{len(items)} items in {path_str}",
            )

        except Exception as e:
            return ToolResponse(
                content=f"Error listing directory: {e}",
                is_error=True,
            )

    def _list_directory(self, path: Path) -> list[tuple[str, str, int]]:
        """List a single directory.

        Args:
            path: Directory path

        Returns:
            List of (type, name, size) tuples
        """
        items = []

        try:
            for entry in sorted(path.iterdir(), key=lambda e: (not e.is_dir(), e.name)):
                # Skip ignored patterns
                if entry.name in self.IGNORE_PATTERNS:
                    continue

                if entry.is_dir():
                    items.append(("DIR", entry.name, 0))
                else:
                    try:
                        size = entry.stat().st_size
                    except Exception:
                        size = 0
                    items.append(("FILE", entry.name, size))

        except PermissionError:
            pass

        return items

    def _list_recursive(self, path: Path) -> list[tuple[str, str, int]]:
        """List directory recursively.

        Args:
            path: Directory path

        Returns:
            List of (type, relative_path, size) tuples
        """
        items = []
        base_path = path

        for root, dirs, files in os.walk(path):
            root_path = Path(root)

            # Filter ignored directories
            dirs[:] = [d for d in dirs if d not in self.IGNORE_PATTERNS]

            # Add directories
            for dir_name in sorted(dirs):
                rel_path = root_path / dir_name
                rel_path = rel_path.relative_to(base_path)
                items.append(("DIR", str(rel_path), 0))

            # Add files
            for file_name in sorted(files):
                file_path = root_path / file_name

                # Skip ignored patterns
                if file_name in self.IGNORE_PATTERNS:
                    continue

                rel_path = file_path.relative_to(base_path)

                try:
                    size = file_path.stat().st_size
                except Exception:
                    size = 0

                items.append(("FILE", str(rel_path), size))

        return items
